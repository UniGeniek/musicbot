const ytdlDiscord = require("ytdl-core-discord");
const scdl = require("soundcloud-downloader");
const { canModifyQueue } = require("../util/EvobotUtil");

module.exports = {
  async play(song, message) {
    let PRUNING, SOUNDCLOUD_CLIENT_ID;

    try {
      const config = require("../config.json");
      PRUNING = config.PRUNING;
      SOUNDCLOUD_CLIENT_ID = config.SOUNDCLOUD_CLIENT_ID;
    } catch (error) {
      PRUNING = process.env.PRUNING;
      SOUNDCLOUD_CLIENT_ID = process.env.SOUNDCLOUD_CLIENT_ID;
    }

    const queue = message.client.queue.get(message.guild.id);

    if (!song) {
      queue.channel.leave();
      message.client.queue.delete(message.guild.id);
      return queue.textChannel.send("üö´ –®–∞—Ä–º–∞–Ω–∫–∞ –∑–∞–∫–æ–Ω—á–∏–ª–∞—Å—å, —Ä–∞—Å—Ö–æ–¥–∏–º—Å—è –∏–ª–∏ –∑–∞–∫–∞–∑—ã–≤–∞–µ–º –¥–∞–ª—å—à–µ!").catch(console.error);
    }

    let stream = null;
    let streamType = song.url.includes("youtube.com") ? "opus" : "ogg/opus";

    try {
      if (song.url.includes("youtube.com")) {
        stream = await ytdlDiscord(song.url, { highWaterMark: 1 << 25 });
      } else if (song.url.includes("soundcloud.com")) {
        try {
          stream = await scdl.downloadFormat(song.url, scdl.FORMATS.OPUS, SOUNDCLOUD_CLIENT_ID);
        } catch (error) {
          stream = await scdl.downloadFormat(song.url, scdl.FORMATS.MP3, SOUNDCLOUD_CLIENT_ID);
          streamType = "unknown";
        }
      }
    } catch (error) {
      if (queue) {
        queue.songs.shift();
        module.exports.play(queue.songs[0], message);
      }

      console.error(error);
      return message.channel.send(`–û—à–∏–±–∫–∞: ${error.message ? error.message : error}`);
    }

    queue.connection.on("disconnect", () => message.client.queue.delete(message.guild.id));

    const dispatcher = queue.connection
      .play(stream, { type: streamType })
      .on("finish", () => {
        if (collector && !collector.ended) collector.stop();

        if (queue.loop) {
          // if loop is on, push the song back at the end of the queue
          // so it can repeat endlessly
          let lastSong = queue.songs.shift();
          queue.songs.push(lastSong);
          module.exports.play(queue.songs[0], message);
        } else {
          // Recursively play the next song
          queue.songs.shift();
          module.exports.play(queue.songs[0], message);
        }
      })
      .on("error", (err) => {
        console.error(err);
        queue.songs.shift();
        module.exports.play(queue.songs[0], message);
      });
    dispatcher.setVolumeLogarithmic(queue.volume / 100);

    try {
      var playingMessage = await queue.textChannel.send(`üé∂ –ü–µ—Å–Ω—è –≤ –ø–ª–µ–π–ª–∏—Å—Ç–µ: **${song.title}** ${song.url}`);
      await playingMessage.react("‚è≠");
      await playingMessage.react("‚èØ");
      await playingMessage.react("üîá");
      await playingMessage.react("üîâ");
      await playingMessage.react("üîä");
      await playingMessage.react("üîÅ");
      await playingMessage.react("‚èπ");
    } catch (error) {
      console.error(error);
    }

    const filter = (reaction, user) => user.id !== message.client.user.id;
    var collector = playingMessage.createReactionCollector(filter, {
      time: song.duration > 0 ? song.duration * 1000 : 600000
    });

    collector.on("collect", (reaction, user) => {
      if (!queue) return;
      const member = message.guild.member(user);

      switch (reaction.emoji.name) {
        case "‚è≠":
          queue.playing = true;
          reaction.users.remove(user).catch(console.error);
          if (!canModifyQueue(member)) return;
          queue.connection.dispatcher.end();
          queue.textChannel.send(`${user} ‚è© –ü–µ—Å–Ω—è –ø—Ä–æ–ø—É—â–µ–Ω–∞`).catch(console.error);
          collector.stop();
          break;

        case "‚èØ":
          reaction.users.remove(user).catch(console.error);
          if (!canModifyQueue(member)) return;
          if (queue.playing) {
            queue.playing = !queue.playing;
            queue.connection.dispatcher.pause(true);
            queue.textChannel.send(`${user} ‚è∏ –ø–æ—Å—Ç–∞–≤–∏–ª —à–∞—Ä–º–∞–Ω–∫—É –Ω–∞ –ø–∞—É–∑—É.`).catch(console.error);
          } else {
            queue.playing = !queue.playing;
            queue.connection.dispatcher.resume();
            queue.textChannel.send(`${user} ‚ñ∂ –≤–∫–ª—é—á–∏–ª –æ–±—Ä–∞—Ç–Ω–æ –ø–µ—Å–Ω–∏ —Ö–æ—Ç—å —Ç—Ä–µ—Å–Ω–∏!`).catch(console.error);
          }
          break;

        case "üîá":
          reaction.users.remove(user).catch(console.error);
          if (!canModifyQueue(member)) return;
          if (queue.volume <= 0) {
            queue.volume = 100;
            queue.connection.dispatcher.setVolumeLogarithmic(100 / 100);
            queue.textChannel.send(`${user} üîä —Ä–∞–∑–º—É—Ç–∏–ª –º—É–∑—ã–∫—É!`).catch(console.error);
          } else {
            queue.volume = 0;
            queue.connection.dispatcher.setVolumeLogarithmic(0);
            queue.textChannel.send(`${user} üîá –∑–∞–º—É—Ç–∏–ª –º—É–∑—ã–∫—É!`).catch(console.error);
          }
          break;

        case "üîâ":
          reaction.users.remove(user).catch(console.error);
          if (!canModifyQueue(member) || queue.volume == 0) return;
          if (queue.volume - 10 <= 0) queue.volume = 0;
          else queue.volume = queue.volume - 10;
          queue.connection.dispatcher.setVolumeLogarithmic(queue.volume / 100);
          queue.textChannel
            .send(`${user} üîâ –ø–æ–º–µ–Ω—è–ª –≥—Ä–æ–º–∫–æ—Å—Ç—å, —Ç–µ–ø–µ—Ä—å –≥—Ä–æ–º–∫–æ—Å—Ç—å: ${queue.volume}%`)
            .catch(console.error);
          break;

        case "üîä":
          reaction.users.remove(user).catch(console.error);
          if (!canModifyQueue(member) || queue.volume == 100) return;
          if (queue.volume + 10 >= 100) queue.volume = 100;
          else queue.volume = queue.volume + 10;
          queue.connection.dispatcher.setVolumeLogarithmic(queue.volume / 100);
          queue.textChannel
            .send(`${user} üîä –ø–æ–º–µ–Ω—è–ª –≥—Ä–æ–º–∫–æ—Å—Ç—å, —Ç–µ–ø–µ—Ä—å –≥—Ä–æ–º–∫–æ—Å—Ç—å: ${queue.volume}%`)
            .catch(console.error);
          break;

        case "üîÅ":
          reaction.users.remove(user).catch(console.error);
          if (!canModifyQueue(member)) return;
          queue.loop = !queue.loop;
          queue.textChannel.send(`–ü–æ–≤—Ç–æ—Ä –ø–µ—Å–Ω–∏: ${queue.loop ? "**–≤–∫–ª.**" : "**–≤—ã–∫–ª.**"}`).catch(console.error);
          break;

        case "‚èπ":
          reaction.users.remove(user).catch(console.error);
          if (!canModifyQueue(member)) return;
          queue.songs = [];
          queue.textChannel.send(`${user} ‚èπ –æ—Å—Ç–∞–Ω–æ–≤–∏–ª –º—É–∑—ã–∫—É!`).catch(console.error);
          try {
            queue.connection.dispatcher.end();
          } catch (error) {
            console.error(error);
            queue.connection.disconnect();
          }
          collector.stop();
          break;

        default:
          reaction.users.remove(user).catch(console.error);
          break;
      }
    });

    collector.on("end", () => {
      playingMessage.reactions.removeAll().catch(console.error);
      if (PRUNING && playingMessage && !playingMessage.deleted) {
        playingMessage.delete({ timeout: 3000 }).catch(console.error);
      }
    });
  }
};
